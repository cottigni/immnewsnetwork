	<p>
		This article attempts to teach you with <strong>some of the best practices of one of the most widely used programming languages</strong> in the world, <strong>PHP</strong>. There are many beginners or even experienced PHP developers who don’t bother to follow the best practices of the language, either unintentionally or intentionally.
	</p>
	<p>	
		Quite often, many people loose many high-paying jobs just because they have never put their attention to the best practices of the language. And it’s really very difficult for anybody to get hold of the best practices overnight.
	</p>
	<p>	
		So, if you <strong>want to be a professional developer</strong> then you must know, understand and follow the best practices of the language and should start practicing them from the very next piece of code you write. The tips are below the following image:
	</p>
	<img src="../images/phpimage.jpg" alt="image depicting a computer screen with php code on it" width="500" height="300"/>

	<p>
		<ol>
			<li>
			<strong>Maintain a Proper Documentation of your Code</strong>
			<div>Maintaining a proper documentation of your code is more important than you had ever thought of. It’s really very unfortunate seeing some beginners or even seasoned programmers not focussing on writing meaningful comments to their codes just because they are too lazy.</div>
			</li>
			<li>
			<strong>Maintain a Proper Coding Standard</strong>
			<div>It is very essential to maintain a proper coding standard, as it can become a painful problem if different programmers (working on a same project) starts using different coding standards.</div>
			</li>
			<li>
			<strong>Never Use Short Tags</strong>
			<div>There are many programmers, I don’t know why, who tries to take a shortcut way of declaring PHP.  Using “<?” or “<%” is never a good practice and it doesn’t make your code look more professional than others.</div>
			</li>
			<li>
			<strong>Use Meaningful Variable and Function Names in your code</strong>
			<div>It can surely become a painful experience for other programmers to understand your code if you don’t stick with a proper naming standard and use generic and un-meaningful names everywhere. It’s not a good practice at all, and you must not follow it in any way.</div>
			</li>
			<li>
			<strong>Indentation, White Spacing and Line Length</strong>
			<div>I have already mentioned the importance of maintaining a proper coding standard, but the things that you need to specifically consider are indentation, white spaces and line lengths.</div>
			</li>
			<li>
			<strong>Single Quoted vs. Double Quoted Strings</strong>
			<div>You have to understand the difference between the single quoted strings and the double quoted ones. If you just have a simple string to display, then always go for single quotes.</div>
			</li>
			<li>
			<strong>Never Use Functions inside Loops</strong>
			<div>I have seen many programmers who tend to make the mistake of using functions inside of loops. If you are doing this intentionally and is ready to compromise performance just for the sake of saving a line of code, then you certainly need to think once again.</div>
			</li>
			<li>
			<strong>Using Single Quotes around Array Indexes</strong>
			<div>There is a difference between $array[‘quotes’] and $array[quotes] and you have to understand this difference properly.</div>
			</li>
			<li>
			<strong>Using the DRY Approach</strong>
			<div>DRY or Don’t Repeat Yourself is a programming concept in software engineering, which tries to cut down redundancy from your code.</div>
			</li>
			<li>
			<strong>Try to Prevent Deep Nesting</strong>
			<div>Try to prevent deep nesting levels in your code, as much as possible. It can make things really difficult for you when you need to debug your code and can take the heck out of people who will try to review your code.</div>
			</li>

		</ol>
	</p>
