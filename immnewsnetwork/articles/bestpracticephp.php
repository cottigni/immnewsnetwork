<h1>10 PHP Best Practices That You Must Follow</h1>
	<h4>written by Aritra Roy</h4>

	<br>
	<form action="process-likes.php" enctype="multipart/form-data" method="POST">
	<button name="like" id="4" type="button">Like</button>
